#FUNCOES
def DT():
    return HC-HS

def MED():
    return round(DP/DT(),2)

#VARIAVEIS
print("===== MEDIDOR DE VELOCIDADE MEDIA =====")
print("Digite a distancia percorrida:")
DP = float(input())

print ("Digite o horário de Saída:")
HS = float(input())

print ("Digite o horário de Chegada:")
HC = float(input())

#RESUTADO
print ("==================== Resultado =====================")
print ("O tempo de duração da viagem foi de", DT(), "hora(s)")
print ("A velocidade média foi de ", MED() ," km/h")
